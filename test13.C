//题目:打印出所有的"水仙花数"，所谓"水仙花数"是指一个三位数，其各位数字立方和等于该数 本身。例如：153是一个"水仙花数"，因为153=1的三次方＋5的三次方＋3的三次方
//ps:可以用for循环,麻烦
#include <stdio.h>
int main()
{
	int m,i,j,k;
	for (m = 100;m <= 999;m++)
	{
		i = m / 100;
		j = m / 10 % 10;
		k = m % 10;
		if (m == i*i*i + j*j*j + k*k*k)
		{
			printf("%d\n",m);
		}
	}
	return 0;
}
